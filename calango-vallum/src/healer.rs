//! HEALER Agent — auto-correction using Gemini for diagnosis and fix.
//!
//! The HEALER Agent monitors agent failures and attempts automatic correction:
//! 1. Diagnoses failures using Gemini AI analysis of error logs and recent messages
//! 2. Tests proposed fixes in an isolated sandbox with 5 test queries
//! 3. Applies fixes via atomic deploy, with rollback on failure
//! 4. Rate-limits attempts to max 3 per agent per hour (sliding window)

use std::collections::HashMap;
use std::future::Future;

use serde::{Deserialize, Serialize};

// =============================================================================
// Types
// =============================================================================

/// Diagnosis produced by Gemini analysis of an agent failure.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct HealerDiagnosis {
    /// The agent that failed.
    pub agent_id: String,
    /// Error logs collected from the failing agent.
    pub error_logs: Vec<String>,
    /// Last messages processed by the agent (up to 10).
    pub last_messages: Vec<String>,
    /// The proposed fix generated by Gemini analysis.
    pub proposed_fix: String,
}

/// Errors that can occur during healing operations.
#[derive(Debug, Clone, PartialEq, Eq, thiserror::Error)]
pub enum HealerError {
    #[error("rate limit exceeded for agent {0}: max {1} attempts per hour")]
    RateLimitExceeded(String, u8),
    #[error("diagnosis failed for agent {0}: {1}")]
    DiagnosisFailed(String, String),
    #[error("sandbox test failed for agent {0}")]
    SandboxTestFailed(String),
    #[error("deployment failed for agent {0}: {1}")]
    DeployFailed(String, String),
    #[error("rollback failed for agent {0}: {1}")]
    RollbackFailed(String, String),
}

// =============================================================================
// Traits (for mockability)
// =============================================================================

/// Trait for Gemini-based failure analysis.
/// Allows mocking the AI analysis in tests.
pub trait GeminiAnalyzer: Send + Sync {
    /// Analyze agent failure using error logs, last messages, and agent state.
    /// Returns a diagnosis with a proposed fix.
    fn analyze_failure(
        &self,
        agent_id: &str,
        error_logs: &[String],
        last_messages: &[String],
    ) -> impl Future<Output = Result<HealerDiagnosis, HealerError>> + Send;
}

/// Trait for sandbox-based fix testing.
/// Allows mocking the sandbox environment in tests.
pub trait HealerSandbox: Send + Sync {
    /// Test a proposed fix in an isolated sandbox with the given number of test queries.
    /// Returns true if all test queries pass.
    fn test_fix_in_sandbox(
        &self,
        diagnosis: &HealerDiagnosis,
        test_queries: usize,
    ) -> impl Future<Output = Result<bool, HealerError>> + Send;
}

/// Trait for atomic deployment and rollback.
/// Allows mocking the deployment process in tests.
pub trait HealerDeployer: Send + Sync {
    /// Atomically deploy the proposed fix for the given agent.
    fn atomic_deploy(
        &self,
        agent_id: &str,
        proposed_fix: &str,
    ) -> impl Future<Output = Result<(), HealerError>> + Send;

    /// Rollback the last deployment for the given agent.
    fn rollback(
        &self,
        agent_id: &str,
    ) -> impl Future<Output = Result<(), HealerError>> + Send;
}

// =============================================================================
// HealerAgent
// =============================================================================

/// Number of test queries to run in sandbox validation.
const SANDBOX_TEST_QUERIES: usize = 5;

/// Duration of the sliding window for rate limiting (1 hour in seconds).
const RATE_LIMIT_WINDOW_SECS: u64 = 3600;

/// The HEALER Agent that orchestrates auto-correction of failing agents.
///
/// Rate-limits healing attempts to `max_attempts_per_hour` per agent using
/// a sliding window approach.
pub struct HealerAgent<A, S, D>
where
    A: GeminiAnalyzer,
    S: HealerSandbox,
    D: HealerDeployer,
{
    /// Maximum healing attempts allowed per agent per hour.
    pub max_attempts_per_hour: u8,
    /// Tracks timestamps (Unix seconds) of healing attempts per agent.
    pub attempt_tracker: HashMap<String, Vec<u64>>,
    /// Gemini analyzer for diagnosing failures.
    analyzer: A,
    /// Sandbox for testing proposed fixes.
    sandbox: S,
    /// Deployer for atomic deploy and rollback.
    deployer: D,
}

impl<A, S, D> HealerAgent<A, S, D>
where
    A: GeminiAnalyzer,
    S: HealerSandbox,
    D: HealerDeployer,
{
    /// Create a new HealerAgent with the given dependencies.
    pub fn new(analyzer: A, sandbox: S, deployer: D) -> Self {
        Self {
            max_attempts_per_hour: 3,
            attempt_tracker: HashMap::new(),
            analyzer,
            sandbox,
            deployer,
        }
    }

    /// Check if the agent has not exceeded the max attempts per hour.
    ///
    /// Uses a sliding window: counts attempts within the last 60 minutes
    /// from `now_secs`. Returns `true` if a new attempt is allowed.
    pub fn can_attempt(&self, agent_id: &str, now_secs: u64) -> bool {
        let window_start = now_secs.saturating_sub(RATE_LIMIT_WINDOW_SECS);

        match self.attempt_tracker.get(agent_id) {
            Some(timestamps) => {
                let recent_count = timestamps
                    .iter()
                    .filter(|&&ts| ts > window_start)
                    .count();
                recent_count < self.max_attempts_per_hour as usize
            }
            None => true,
        }
    }

    /// Record a healing attempt for the given agent at the specified time.
    fn record_attempt(&mut self, agent_id: &str, now_secs: u64) {
        let timestamps = self
            .attempt_tracker
            .entry(agent_id.to_string())
            .or_default();
        timestamps.push(now_secs);

        // Prune old entries outside the window to prevent unbounded growth.
        let window_start = now_secs.saturating_sub(RATE_LIMIT_WINDOW_SECS);
        timestamps.retain(|&ts| ts > window_start);
    }

    /// Diagnose an agent failure using Gemini analysis.
    ///
    /// Analyzes error logs and the last 10 messages to produce a diagnosis
    /// with a proposed fix.
    pub async fn diagnose(
        &self,
        agent_id: &str,
        error_logs: &[String],
        last_messages: &[String],
    ) -> Result<HealerDiagnosis, HealerError> {
        // Take at most the last 10 messages for analysis.
        let messages: Vec<String> = last_messages
            .iter()
            .rev()
            .take(10)
            .rev()
            .cloned()
            .collect();

        self.analyzer
            .analyze_failure(agent_id, error_logs, &messages)
            .await
    }

    /// Test a proposed fix in an isolated sandbox with 5 test queries.
    ///
    /// Returns `true` if all test queries pass in the sandbox.
    pub async fn test_fix(
        &self,
        diagnosis: &HealerDiagnosis,
    ) -> Result<bool, HealerError> {
        self.sandbox
            .test_fix_in_sandbox(diagnosis, SANDBOX_TEST_QUERIES)
            .await
    }

    /// Apply a fix via atomic deploy if sandbox test passes, rollback if it fails.
    ///
    /// This method:
    /// 1. Checks rate limit
    /// 2. Deploys the fix atomically
    /// 3. Rolls back on deployment failure
    pub async fn apply_fix(
        &mut self,
        diagnosis: &HealerDiagnosis,
        now_secs: u64,
    ) -> Result<(), HealerError> {
        let agent_id = &diagnosis.agent_id;

        // Check rate limit before attempting.
        if !self.can_attempt(agent_id, now_secs) {
            return Err(HealerError::RateLimitExceeded(
                agent_id.clone(),
                self.max_attempts_per_hour,
            ));
        }

        // Record the attempt.
        self.record_attempt(agent_id, now_secs);

        // Attempt atomic deploy.
        match self
            .deployer
            .atomic_deploy(agent_id, &diagnosis.proposed_fix)
            .await
        {
            Ok(()) => Ok(()),
            Err(deploy_err) => {
                // Rollback on failure (best-effort).
                let _ = self.deployer.rollback(agent_id).await;
                Err(deploy_err)
            }
        }
    }
}

