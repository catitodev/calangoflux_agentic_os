#[cfg(test)]
mod tests {
    use super::*;

    // =========================================================================
    // Mock implementations for testing
    // =========================================================================

    struct MockAnalyzer;

    impl MockAnalyzer {
        fn new() -> Self {
            Self
        }
    }

    impl GeminiAnalyzer for MockAnalyzer {
        async fn analyze_failure(
            &self,
            agent_id: &str,
            error_logs: &[String],
            _last_messages: &[String],
        ) -> Result<HealerDiagnosis, HealerError> {
            Ok(HealerDiagnosis {
                agent_id: agent_id.to_string(),
                error_logs: error_logs.to_vec(),
                last_messages: vec![],
                proposed_fix: "restart service".to_string(),
            })
        }
    }

    struct MockSandbox {
        pass: bool,
    }

    impl MockSandbox {
        fn new(pass: bool) -> Self {
            Self { pass }
        }
    }

    impl HealerSandbox for MockSandbox {
        async fn test_fix_in_sandbox(
            &self,
            _diagnosis: &HealerDiagnosis,
            _test_queries: usize,
        ) -> Result<bool, HealerError> {
            Ok(self.pass)
        }
    }

    struct MockDeployer {
        should_fail: bool,
    }

    impl MockDeployer {
        fn new(should_fail: bool) -> Self {
            Self { should_fail }
        }
    }

    impl HealerDeployer for MockDeployer {
        async fn atomic_deploy(
            &self,
            agent_id: &str,
            _proposed_fix: &str,
        ) -> Result<(), HealerError> {
            if self.should_fail {
                Err(HealerError::DeployFailed(
                    agent_id.to_string(),
                    "mock deploy failure".to_string(),
                ))
            } else {
                Ok(())
            }
        }

        async fn rollback(&self, _agent_id: &str) -> Result<(), HealerError> {
            Ok(())
        }
    }

    // =========================================================================
    // Rate limiting tests
    // =========================================================================

    #[test]
    fn test_can_attempt_no_prior_attempts() {
        let healer = HealerAgent::new(
            MockAnalyzer::new(),
            MockSandbox::new(true),
            MockDeployer::new(false),
        );
        assert!(healer.can_attempt("agent-1", 1000));
    }

    #[test]
    fn test_can_attempt_under_limit() {
        let mut healer = HealerAgent::new(
            MockAnalyzer::new(),
            MockSandbox::new(true),
            MockDeployer::new(false),
        );
        // Record 2 attempts within the window.
        healer.record_attempt("agent-1", 1000);
        healer.record_attempt("agent-1", 2000);

        // Should still allow a 3rd attempt.
        assert!(healer.can_attempt("agent-1", 3000));
    }

    #[test]
    fn test_can_attempt_at_limit() {
        let mut healer = HealerAgent::new(
            MockAnalyzer::new(),
            MockSandbox::new(true),
            MockDeployer::new(false),
        );
        // Record 3 attempts within the window.
        healer.record_attempt("agent-1", 1000);
        healer.record_attempt("agent-1", 2000);
        healer.record_attempt("agent-1", 3000);

        // 4th attempt should be rejected.
        assert!(!healer.can_attempt("agent-1", 3500));
    }

    #[test]
    fn test_can_attempt_after_window_expires() {
        let mut healer = HealerAgent::new(
            MockAnalyzer::new(),
            MockSandbox::new(true),
            MockDeployer::new(false),
        );
        // Record 3 attempts at time 1000, 1500, 2000.
        healer.record_attempt("agent-1", 1000);
        healer.record_attempt("agent-1", 1500);
        healer.record_attempt("agent-1", 2000);

        // After 1 hour (3600s), the window has slid past the first attempt.
        // now = 4601 means window_start = 4601 - 3600 = 1001
        // Attempts at 1000 are outside (not > 1001), 1500 and 2000 are inside → 2 attempts.
        assert!(healer.can_attempt("agent-1", 4601));
    }

    #[test]
    fn test_can_attempt_sliding_window_partial_expiry() {
        let mut healer = HealerAgent::new(
            MockAnalyzer::new(),
            MockSandbox::new(true),
            MockDeployer::new(false),
        );
        // Attempts at t=100, t=200, t=300.
        healer.record_attempt("agent-1", 100);
        healer.record_attempt("agent-1", 200);
        healer.record_attempt("agent-1", 300);

        // At t=3701, window_start = 3701 - 3600 = 101.
        // Only t=200 and t=300 are > 101 → 2 attempts, allow 3rd.
        assert!(healer.can_attempt("agent-1", 3701));

        // At t=3700, window_start = 3700 - 3600 = 100.
        // t=100 is NOT > 100 (equal), so excluded. t=200, t=300 in window → 2 attempts.
        assert!(healer.can_attempt("agent-1", 3700));
    }

    #[test]
    fn test_can_attempt_different_agents_independent() {
        let mut healer = HealerAgent::new(
            MockAnalyzer::new(),
            MockSandbox::new(true),
            MockDeployer::new(false),
        );
        // Max out agent-1.
        healer.record_attempt("agent-1", 1000);
        healer.record_attempt("agent-1", 2000);
        healer.record_attempt("agent-1", 3000);

        // agent-1 is at limit.
        assert!(!healer.can_attempt("agent-1", 3500));
        // agent-2 is independent and has no attempts.
        assert!(healer.can_attempt("agent-2", 3500));
    }

    #[test]
    fn test_record_attempt_prunes_old_entries() {
        let mut healer = HealerAgent::new(
            MockAnalyzer::new(),
            MockSandbox::new(true),
            MockDeployer::new(false),
        );
        // Record old attempts.
        healer.record_attempt("agent-1", 100);
        healer.record_attempt("agent-1", 200);

        // Record a new attempt far in the future — old ones should be pruned.
        healer.record_attempt("agent-1", 5000);

        let timestamps = healer.attempt_tracker.get("agent-1").unwrap();
        // Only the recent one (5000) should remain since window_start = 5000 - 3600 = 1400.
        assert_eq!(timestamps.len(), 1);
        assert_eq!(timestamps[0], 5000);
    }

    #[tokio::test]
    async fn test_apply_fix_respects_rate_limit() {
        let mut healer = HealerAgent::new(
            MockAnalyzer::new(),
            MockSandbox::new(true),
            MockDeployer::new(false),
        );

        let diagnosis = HealerDiagnosis {
            agent_id: "agent-1".to_string(),
            error_logs: vec!["error".to_string()],
            last_messages: vec![],
            proposed_fix: "fix".to_string(),
        };

        // First 3 attempts should succeed.
        assert!(healer.apply_fix(&diagnosis, 1000).await.is_ok());
        assert!(healer.apply_fix(&diagnosis, 2000).await.is_ok());
        assert!(healer.apply_fix(&diagnosis, 3000).await.is_ok());

        // 4th attempt within the hour should fail with rate limit error.
        let result = healer.apply_fix(&diagnosis, 3500).await;
        assert!(matches!(result, Err(HealerError::RateLimitExceeded(_, 3))));
    }

    #[tokio::test]
    async fn test_apply_fix_rollback_on_deploy_failure() {
        let mut healer = HealerAgent::new(
            MockAnalyzer::new(),
            MockSandbox::new(true),
            MockDeployer::new(true), // deployer will fail
        );

        let diagnosis = HealerDiagnosis {
            agent_id: "agent-1".to_string(),
            error_logs: vec![],
            last_messages: vec![],
            proposed_fix: "bad fix".to_string(),
        };

        let result = healer.apply_fix(&diagnosis, 1000).await;
        assert!(matches!(result, Err(HealerError::DeployFailed(_, _))));
    }

    #[tokio::test]
    async fn test_diagnose_limits_to_last_10_messages() {
        let healer = HealerAgent::new(
            MockAnalyzer::new(),
            MockSandbox::new(true),
            MockDeployer::new(false),
        );

        let messages: Vec<String> = (0..20).map(|i| format!("msg-{}", i)).collect();
        let errors = vec!["error".to_string()];

        let diagnosis = healer.diagnose("agent-1", &errors, &messages).await.unwrap();
        assert_eq!(diagnosis.agent_id, "agent-1");
    }

    #[tokio::test]
    async fn test_test_fix_passes() {
        let healer = HealerAgent::new(
            MockAnalyzer::new(),
            MockSandbox::new(true),
            MockDeployer::new(false),
        );

        let diagnosis = HealerDiagnosis {
            agent_id: "agent-1".to_string(),
            error_logs: vec![],
            last_messages: vec![],
            proposed_fix: "fix".to_string(),
        };

        assert!(healer.test_fix(&diagnosis).await.unwrap());
    }

    #[tokio::test]
    async fn test_test_fix_fails() {
        let healer = HealerAgent::new(
            MockAnalyzer::new(),
            MockSandbox::new(false), // sandbox will report failure
            MockDeployer::new(false),
        );

        let diagnosis = HealerDiagnosis {
            agent_id: "agent-1".to_string(),
            error_logs: vec![],
            last_messages: vec![],
            proposed_fix: "bad fix".to_string(),
        };

        assert!(!healer.test_fix(&diagnosis).await.unwrap());
    }
}
