/// A message on the bus, used by SHIELD for observation.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct BusMessage {
    pub id: String,
    pub sender_id: AgentId,
    pub destination_id: AgentId,
    pub task_type: String,
    pub payload: Vec<u8>,
    pub timestamp: u64,
}

