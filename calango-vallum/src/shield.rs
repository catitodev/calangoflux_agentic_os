/// Severity levels for security alerts.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum AlertSeverity {
    Low,
    Medium,
    High,
    Critical,
}

/// Types of security alerts the SHIELD agent can raise.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum AlertType {
    CredentialExposure,
    RateLimitExceeded,
    AnomalyDetected,
}

/// A security alert generated by the SHIELD agent.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct SecurityAlert {
    pub alert_type: AlertType,
    pub agent_id: AgentId,
    pub severity: AlertSeverity,
    pub details: String,
    pub timestamp: u64,
}

// =============================================================================
// Rate Counter
// =============================================================================

/// Tracks actions per agent within a sliding 60-second window.
/// Stores timestamps (in seconds) of each action in a VecDeque.
#[derive(Debug, Clone)]
pub struct RateCounter {
    /// Timestamps (unix seconds) of actions within the current window.
    pub actions: VecDeque<u64>,
}

impl RateCounter {
    pub fn new() -> Self {
        Self {
            actions: VecDeque::new(),
        }
    }

    /// Record an action at the given timestamp and prune expired entries.
    pub fn record_action(&mut self, timestamp_secs: u64) {
        // Remove actions older than 60 seconds
        let cutoff = timestamp_secs.saturating_sub(60);
        while let Some(&front) = self.actions.front() {
            if front <= cutoff {
                self.actions.pop_front();
            } else {
                break;
            }
        }
        self.actions.push_back(timestamp_secs);
    }

    /// Returns the number of actions in the current 60-second window.
    pub fn count(&self, current_timestamp_secs: u64) -> usize {
        let cutoff = current_timestamp_secs.saturating_sub(60);
        self.actions.iter().filter(|&&t| t > cutoff).count()
    }
}

impl Default for RateCounter {
    fn default() -> Self {
        Self::new()
    }
}

// =============================================================================
// Rolling Baseline (Anomaly Detection)
// =============================================================================

/// A rolling baseline over a configurable window (default 24 hours).
/// Maintains data points and computes mean and standard deviation for anomaly detection.
#[derive(Debug, Clone)]
pub struct RollingBaseline {
    /// Window size in hours.
    pub window_hours: u32,
    /// Data points within the rolling window.
    pub data_points: Vec<f64>,
    /// Current mean of data points.
    pub mean: f64,
    /// Current standard deviation of data points.
    pub std_dev: f64,
}

impl RollingBaseline {
    /// Create a new rolling baseline with a 24-hour window.
    pub fn new() -> Self {
        Self {
            window_hours: 24,
            data_points: Vec::new(),
            mean: 0.0,
            std_dev: 0.0,
        }
    }

    /// Create a rolling baseline with a custom window size.
    pub fn with_window(window_hours: u32) -> Self {
        Self {
            window_hours,
            data_points: Vec::new(),
            mean: 0.0,
            std_dev: 0.0,
        }
    }

    /// Add a data point and recompute statistics.
    pub fn add_data_point(&mut self, value: f64) {
        self.data_points.push(value);
        self.recompute_stats();
    }

    /// Recompute mean and standard deviation from current data points.
    fn recompute_stats(&mut self) {
        let n = self.data_points.len();
        if n == 0 {
            self.mean = 0.0;
            self.std_dev = 0.0;
            return;
        }

        let sum: f64 = self.data_points.iter().sum();
        self.mean = sum / n as f64;

        if n < 2 {
            self.std_dev = 0.0;
            return;
        }

        let variance: f64 = self
            .data_points
            .iter()
            .map(|&x| (x - self.mean).powi(2))
            .sum::<f64>()
            / n as f64;
        self.std_dev = variance.sqrt();
    }

    /// Check if a value is anomalous (>2 standard deviations from mean).
    /// Returns false if there are fewer than 2 data points (insufficient data).
    pub fn is_anomalous(&self, value: f64) -> bool {
        if self.data_points.len() < 2 || self.std_dev == 0.0 {
            return false;
        }
        let deviation = (value - self.mean).abs();
        deviation > 2.0 * self.std_dev
    }
}

impl Default for RollingBaseline {
    fn default() -> Self {
        Self::new()
    }
}

