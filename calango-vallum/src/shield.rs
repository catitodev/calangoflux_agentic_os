/// Known credential/secret patterns to detect in message payloads.
const CREDENTIAL_PATTERNS: &[&str] = &[
    "sk-",       // OpenAI / Stripe secret keys
    "AKIA",      // AWS access key IDs
    "ghp_",      // GitHub personal access tokens
    "gho_",      // GitHub OAuth tokens
    "ghu_",      // GitHub user-to-server tokens
    "ghs_",      // GitHub server-to-server tokens
    "Bearer ",   // Bearer tokens in headers
    "bearer ",   // lowercase variant
];

/// The SHIELD Agent monitors all agent actions in real-time.
///
/// Responsibilities:
/// - Detect credential exposure in message payloads
/// - Enforce rate limits (100 actions/min per agent)
/// - Detect anomalies via rolling baseline (>2 std deviations)
#[derive(Debug)]
pub struct ShieldAgent {
    /// Rate limit counters per agent.
    pub rate_limits: HashMap<AgentId, RateCounter>,
    /// Rolling baseline for anomaly detection.
    pub anomaly_baseline: RollingBaseline,
}

impl ShieldAgent {
    /// Create a new ShieldAgent with default settings.
    pub fn new() -> Self {
        Self {
            rate_limits: HashMap::new(),
            anomaly_baseline: RollingBaseline::new(),
        }
    }

    /// Observe a bus message in real-time and return a security alert if a
    /// violation is detected.
    ///
    /// Checks (in order):
    /// 1. Credential exposure in payload
    /// 2. Rate limit exceeded for sender
    pub fn observe_message(&mut self, msg: &BusMessage) -> Option<SecurityAlert> {
        // Check for credential exposure
        if self.detect_credential_exposure(&msg.payload) {
            return Some(SecurityAlert {
                alert_type: AlertType::CredentialExposure,
                agent_id: msg.sender_id.clone(),
                severity: AlertSeverity::Critical,
                details: format!(
                    "Credential pattern detected in message {} from agent {}",
                    msg.id,
                    msg.sender_id.as_str()
                ),
                timestamp: msg.timestamp,
            });
        }

        // Check rate limit
        if self.check_rate_limit(&msg.sender_id, msg.timestamp) {
            return Some(SecurityAlert {
                alert_type: AlertType::RateLimitExceeded,
                agent_id: msg.sender_id.clone(),
                severity: AlertSeverity::High,
                details: format!(
                    "Agent {} exceeded rate limit of 100 actions/min",
                    msg.sender_id.as_str()
                ),
                timestamp: msg.timestamp,
            });
        }

        None
    }

    /// Detect credential exposure patterns in a message payload.
    ///
    /// Scans the payload (interpreted as UTF-8) for known secret patterns:
    /// `sk-`, `AKIA`, `ghp_`, `Bearer` tokens, etc.
    pub fn detect_credential_exposure(&self, payload: &[u8]) -> bool {
        // Try to interpret payload as UTF-8 text
        let text = match std::str::from_utf8(payload) {
            Ok(s) => s,
            Err(_) => return false, // Binary payload, skip text pattern matching
        };

        for pattern in CREDENTIAL_PATTERNS {
            if text.contains(pattern) {
                return true;
            }
        }

        false
    }

    /// Check if an agent exceeds the rate limit (100 actions/min).
    ///
    /// Records the action and returns `true` if the agent has exceeded
    /// 100 actions within the last 60 seconds (meaning it should be throttled).
    pub fn check_rate_limit(&mut self, agent_id: &AgentId, timestamp_ms: u64) -> bool {
        let timestamp_secs = timestamp_ms / 1000;
        let counter = self
            .rate_limits
            .entry(agent_id.clone())
            .or_insert_with(RateCounter::new);

        counter.record_action(timestamp_secs);
        counter.count(timestamp_secs) > 100
    }

    /// Update the anomaly baseline with a new metric data point.
    pub fn update_baseline(&mut self, metric: &MetricPoint) {
        self.anomaly_baseline.add_data_point(metric.value);
    }

    /// Detect if a metric point is anomalous (>2 standard deviations from baseline mean).
    ///
    /// Returns `true` if the value deviates more than 2 standard deviations
    /// from the rolling 24-hour baseline mean.
    pub fn detect_anomaly(&self, metric: &MetricPoint) -> bool {
        self.anomaly_baseline.is_anomalous(metric.value)
    }
}

impl Default for ShieldAgent {
    fn default() -> Self {
        Self::new()
    }
}

